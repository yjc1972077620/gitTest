package gitTest;

public class Git {
	
	private String gitName;
	private String gitPwd;
	
	public void setGitName(String gitName) {
		this.gitName = gitName;
	}
	public void setGitPwd(String gitPwd) {
		this.gitPwd = gitPwd;
	}
	public String getGitName() {
		return gitName;
	}
	public String getGitPwd() {
		return gitPwd;
	}
	
}
